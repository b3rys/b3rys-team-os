/**
 * ★깨진 이스케이프를 펴준다 — 단, 멀쩡한 본문은 절대 건드리지 않는다.★
 *
 * ═══ 실측 (2026-07-13 · 재발 2026-07-14 01:03) ═══
 *   팀장 단톡방에 이렇게 찍혔다:
 *     "[종합 보고] Devon/Dbak 좋아하는 음식 수집 결과입니다.\\n\\n- Devon: 김치찌개\\n- Dbak: …"
 *   ★"\\n" 이 문자 그대로 보인다.★
 *
 * ★[B] 전환의 부작용이다★ — 이제 팀원이 본문을 직접 쓴다. 예전엔 서버가 턴 본문을 그대로 게시해서 이 문제가 없었다.
 *   셸에서 `--body "…\\n\\n…"` = 큰따옴표 안의 \\n 은 ★개행이 아니라 백슬래시+n 두 글자★.
 *   JSON 을 직접 만들 때 이중 이스케이프해도 같다.
 *   ★send.sh 에서 한 번 고쳤는데, 팀원이 API 를 직접 부르면 또 샜다★ (실측: 긴 본문에서 우회).
 *   ★통로가 여럿이면 가드도 여럿이어야 한다 — 그게 [NO_REPLY] 가 샌 이유다.★ → ★모든 발신이 지나는 문에서 막는다.★
 *
 * ★이 테스트의 절반은 "펴야 하는 것", 절반은 "★절대 건드리면 안 되는 것★".★
 * ★후자가 더 중요하다 — 남의 코드를 망가뜨리는 게 \\n 이 보이는 것보다 훨씬 나쁘다.★
 */
import { describe, expect, it } from "bun:test";
import { normalizeEscapedNewlines as fix } from "./acceptInbound";

describe("★깨진 이스케이프를 편다★", () => {
  it("★실측 재현: 리터럴 \\\\n 만 있는 본문★", () => {
    const broken = "[종합 보고] 수집 결과입니다.\\n\\n- Devon: 김치찌개\\n- Dbak: 소고기 뭇국";
    expect(fix(broken)).toBe("[종합 보고] 수집 결과입니다.\n\n- Devon: 김치찌개\n- Dbak: 소고기 뭇국");
  });

  it("★\\n 만 편다 — 탭 등 다른 이스케이프는 안 건드린다★ (안 겪은 문제를 추측으로 처리하지 않는다)", () => {
    expect(fix("A\\tB")).toBe("A\\tB");
  });
});

describe("★★멀쩡한 본문은 절대 건드리지 않는다★★ (이쪽이 더 중요하다)", () => {
  it("★이미 진짜 개행이 있으면 손대지 않는다★ — 작성자가 의도한 형식이다", () => {
    const ok = "종합 결과\n\n- A: 1\n- B: 2";
    expect(fix(ok)).toBe(ok);
  });

  it("★★코드를 망가뜨리지 않는다★★ — 진짜 개행이 있는 코드 안의 \\\\n 은 그대로 둔다", () => {
    const code = 'console.log("hello\\nworld");\nconst re = /\\n/g;';
    expect(fix(code)).toBe(code);   // ★한 글자도 바뀌면 안 된다★
  });

  it("리터럴도 개행도 없는 평범한 한 줄 — 그대로", () => {
    expect(fix("가을이요. 공기가 선명해서 좋습니다.")).toBe("가을이요. 공기가 선명해서 좋습니다.");
  });

  it("빈 문자열", () => expect(fix("")).toBe(""));
});
